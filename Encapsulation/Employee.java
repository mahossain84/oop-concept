package Encapsulation;

public class Employee {
    public String name;
    public int id;
    public int age;
    public double salary;

    public Employee(){

    }

    public Employee(String name, int id, int age, double salary) {
        this.name = name;
        this.id = id;
        this.age = age;
        this.salary = salary;
    }


}
